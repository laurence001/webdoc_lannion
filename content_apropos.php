
<p>
« Les visages du sport » est un webdocumentaire produit par les étudiant.e.s en deuxième année de journalisme de l'IUT de Lannion (promotion 2019 - 2021). Nous avons enquêté sur les enjeux sociaux, sociétaux et économiques du domaine sportif.   
<br>
<br>
Aujourd'hui, deux Français.e.s sur trois pratiquent un sport au moins une fois par semaine. Mais l’activité. physique ne s'arrête pas seulement à des compétitions sportives. Sommes-nous vraiment toustes égaux dans cette pratique ? À travers cinq profils de citoyen.nes-types fictif.ve.s, de toustes âges, genres et classes sociales, ce webdocumentaire vous emmène dans les réalités de ce milieu. Vous y trouverez 14 enquêtes approfondies et déclinées selon différentes trajectoires de vie.
<br>
<br>
Sasha est une adolescente confrontée aux difficultés d’un monde longtemps réservé aux hommes. Retraité, Charlie est aujourd’hui en bonne santé. Sa pratique sportive l’a aidé à guérir de son cancer. Aylan, conseiller municipal, regrette le peu de moyens financiers et matériels attribué au sport. Elio vient d’un quartier populaire. Face à la réalité des moyens financiers et des politiques locales, sa pratique sportive est limitée. Alix est passionnée par le vélo. Depuis son accident, elle peine à trouver des clubs handisports. 
<br>
<br>
Vous pouvez nous retrouver sur Twitter (@LVDSport) et Instagram (@lesvisagesdusport).
</p>
