<p>
Par une fraîche matinée de novembre, la plage de Trestrignel devient un point de  rencontre des seniors. Iels ne sont pas adeptes des pratiques intenses, mais nombre d’entre elleux se baignent régulièrement en eau froide.
</p>

<p> <em>« Je vais dans l’eau toute l’année. À cette époque, elle est encore assez chaude pour  rester dix minutes. Dans un mois ce ne sera plus que cinq »</em>, explique Jean, habitant de Perros-Guirrec, à la carrure solide et à la peau fanée par l’âge. Avec sa compagne, Françoise, iels se  baignent deux fois par semaine, peu importe la température. Le couple, qui habite à  moins d’un kilomètre de la mer, a gardé ses habitudes matinales pendant le deuxième  confinement. <em>« En mars, les plages étaient interdites, c’était énervant, on se sentait  moins bien. C’est mauvais pour la circulation sanguine de ne pas bouger »</em>, déplore le vieil homme encore fringuant. <em>«  D’ailleurs notre médecin nous dit de continuer »</em>.
</p> 
				
<h3 class="inter">10% des licencié·e·s ont plus de 60 ans  </h3>

<p>En 2019, les fédérations françaises comptaient près de 2 millions de licenciée·s de plus de 60  ans. Rapporté au total des licencié·e·s français·e·s cette année-là, les seniors représentaient plus de 10% de l’effectif des associations et clubs sportif·ve·s français·e·s.  
</p>

<p>Alain Calvez fait partie de ces seniors actif·ve·s. Rien n’arrête ce féru de vélo. Pendant le  confinement, tous les deux jours, le sexagénaire parcourait 27 kilomètres, tout en restant dans le  périmètre d’un kilomètre autour de son domicile. <em>« C’est difficile d’avoir la volonté de faire ça tout seul, le groupe permet de sortir de son lit [NDLR, se motiver] »</em>. Parmi la quarantaine de licencié·e·s, <em>« la  majorité des membres du club a complètement arrêté »</em>, note le retraité.
</p>

<h3 class="inter">Des pratiques spécifiques et «&nbsp;bénéfiques&nbsp;» </h3>

<p>Le nombre de licencié·e·s seniors invisibilise une partie des sportif·ve·s âgé·e·s. En effet, la majorité de  l’activité physique des personnes âgées ne se fait ni en club, ni dans une association. La marche, la course à pied ou la natation sont des pratiques privilégiées par les seniors, sans grande répercussion dans les chiffres des fédérations. La Fédération française de natation compte pas moins de 512 015 licencié·e·s. Et pour cause, l’accès aux infrastructures n’est pas réservé aux clubs. 
</p>

<p> En 2009, 16% des Français·e·s entre 70 et 75 ans déclaraient faire partie d’un club ou une  association sportive. Proportion assez faible quand on sait que 34% d’entre elleux faisaient  de l’activité sportive de manière spontanée, pas à la portée de tou·te·s. 
</p>

<p>Karl Chaory, docteur en médecine de réadaptation à Rennes, vante les bénéfices de l’activité physique en groupe chez les publics fragiles.
</p>

<blockquote cite="http://www.worldwildlife.org/who/index.html">
« Les bienfaits du sport touchent la  partie cardio-respiratoire. La pratique sportive apportent aussi des avantages psychologiques et psychosociaux .»
</blockquote>

<p>Le sport permet également de développer une plus grande résistance contre la douleur. Le spécialiste poursuit.Selon lui, c'est aussi une question d’ <em>« amélioration du sommeil et de l’hygiène de vie, parce qu’à partir du moment ou l’on fait du sport, on fait attention à ce que l’on va manger »</em>. Le médecin ne recommande pas de pratique intense, mais de la marche, du vélo ou du renforcement musculaire. Des exercices que l’on peut faire sans club de sport, sans infrastructure spécifique. 
</p>

