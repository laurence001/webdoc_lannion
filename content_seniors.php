<h3 class="inter">Oublié·e·s parmi les oublié·e·s </h3>

<p> D'autres seniors manquent cruellement d'exercice. Présenté·e·s comme les plus fragiles, les résident·e·s d'Établissement d'Hébergement pour Personnes  gées Dépendantes (EHPAD) ont été privé·e·s de mouvement. Confiné·e·s pendant plusieurs mois dans des chambres d’une dizaine de mètres carrés, les seniors ne pouvaient pas se rendre dans les  espaces collectifs. Le service à la chambre a été imposé à des milliers de pensionnaires privé·e·s d’activités de groupe. À la fin du confinement, <em>« certain·e·s ne savent plus marcher, on voit bien qu’on aura besoin de plus de prise en charge par des kinésithérapeutes »</em>, se désole Florence Braud, aide-soignante dans un établissement morbihannais. C’est l’un des symptômes d’une  désadaptation psycho-motrice, un syndrome dû au manque d’activité. <em>« Quand nos  résident·e·s ne pratiquent plus certains gestes pendant un moment, iels en perdent l’habitude »</em>, explique Jacky Fichaux. 
</p>

<blockquote cite="http://www.worldwildlife.org/who/index.html">
« Comme les personnes sont isolées dans leurs chambres, il faut plus de mains  pour les repas, il faut plus de mains pour stimuler les résidents », poursuit l’infirmier cadre dans un EHPAD de Lille.</blockquote>

<p> Si le Nordiste  affirme avoir eu une rallonge de l’Agence Régionale de Santé pour du personnel en  renfort, sa consoeur y voit un problème. <em>« Dans certains EHPAD il y a eu des renforts,  d’autres non, ça dépendait des régions »</em>, observe Florence Braud, avant de raccrocher pour se rendre  au chevet d’un pensionnaire tombé. La majorité des établissements contactés avaient mis leurs animations sportives et culturelles en pause. Les associations et intervenants externes, habituellement chargé·e·s de s’occuper des résident·e·s, trouvent aujourd’hui encore « porte  close ». Et pourtant,  les pensionnaires sont en demande. Marie-Louise Laurent réside à l’EHPAD de Neussargues-en-Pinatelle, dans le Cantal. <em>« On est plus agiles grâce à la  gym, on se sent mieux »</em>, résume la pensionnaire de quatre-vingt-six ans <em>« et demi »</em>, comme  elle tient à le préciser. 
</p>

<p> Les mesures de confinement décrétées par le gouvernement depuis mars avaient pour but de limiter les risques de contamination au coronavirus. Officiellement, les personnes âgées sont une priorité. Pourtant, les conséquences de l’absence de pratique sportive chez les seniors, à la fois sur leur santé mentale et  physique, pourraient avoir été sous-estimée. En voulant protéger la qualité de vie de nos aîné·e·s, c’est peut-être l’inverse qui s’est produit. </p>
